           He moved to Los Angeles, California in 1999, where he also specializes in digital image manipulation, digital printing and direction of photography for documentaries.<br>
          But back in 1996 he began yet another path of exploration: searching for the delicate balance between form, function, art, light and space which resulted in his “Lighting Sculptures.” These pieces instantly attracted lots of attention.<br>
          A unique choice of materials, textures and light combined to create unique pieces where tension, balance, peacefulness and simplicity embark in a joyful play of transparencies and shadows.<br>
